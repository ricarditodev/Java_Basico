package com.company.conceptos;

public class TiposDeDatos {
    public static void main(String[] args) {
        //enteros
        byte number1 = 1; // 1 byte
        short number2 = 2; // 2 bytes
        int number3 = 3; // 4 bytes
        long number4 = 4; // 8 bytes

        //punto flotante
        float decimal1 = 3.3f;
        double decimal2 = 2.2d;

        //caracter
        char caracter1 = 'a';

        //booleanos
        boolean verdadero = true;
        boolean falso = false;

        //cadenas de texto o strings
        String nombre = "Ricardo";
        String apellido = "Aguilar";

        //tipos envoltorio -- sirve para establecer nulos
        Integer numero = null;
        Long numero2 = 2L;

    }
}
